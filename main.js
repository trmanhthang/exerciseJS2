var inputWidth = prompt('Nhập chiều dài');
var inputHeight = prompt('Nhập chiều rộng');
var width = parseInt(inputWidth);
var height = parseInt(inputHeight);
var area = width * height;

document.write('Chiều dài là:' + width);
document.write('<br>');
document.write('Chiều rộng là:' + height);
document.write('<br>');
document.write('Diện tích là: ' + area);

